package 배열1;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Q3 {
	/* 세 개의 자연수 A, B, C가 주어질 때 
	   A×B×C를 계산한 결과에 0부터 9까지 각각의 숫자가 몇 번씩 쓰였는지를 구하는 프로그램을 작성하시오. 
	   예를 들어 A = 150, B = 266, C = 427 이라면 
       A × B × C = 150 × 266 × 427 = 17037300 이 되고, 
	   계산한 결과 17037300 에는 0이 3번, 1이 1번, 3이 2번, 7이 2번 쓰였다.
	*/
	
	public static void main(String[] args) throws NumberFormatException, IOException {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		
		int num[] = new int[3];
		for (int i = 0; i < num.length; i++) {
			num[i] = Integer.parseInt(br.readLine());
		}
		
		int A = num[0];
		int B = num[1];
		int C = num[2];
		
		int abs = A * B * C;
		String str = String.valueOf(abs); // 세 숫자를 곱한 후 문자열로 형변환
		
		int arr[] = new int[10]; // 0~9까지 나온 개수를 담기 위한 배열 선언
		
		for (int i = 0; i < str.length(); i++) { // 문자열로 변환한 숫자의 길이 만큼 for문 실행
			arr[(str.charAt(i) - 48)]++; // 유니코드이기 때문에 -48
		}
		
		for (int v : arr) {
			System.out.println(v);
		}
		
	}
}
